    private void populateTable() {
        
        DefaultTableModel model =(DefaultTableModel) tblDisplay.getModel();
        model.setRowCount(0);
        
        for(ServiceRecord sr : carProfile.getServiceRecords().getServiceRecords()){
            Object[] row = new Object[3];
            row[0] = sr.getDate();
            row[1] = sr.getCharges();
            row[2] = sr.getParts();
            
            model.addRow(row);
        }
        
        
        
        
    }
